// Helper functions
export function getRookiesByPosition(position) {
  return ROOKIES_2026.filter(r => r.position === position)
    .sort((a, b) => b.projectedValue - a.projectedValue);
}

export function getRookieById(id) {
  return ROOKIES_2026.find(r => r.id === id);
}

export function getRookiesByValue() {
  return [...ROOKIES_2026].sort((a, b) => b.projectedValue - a.projectedValue);
}

export function getRookiesByADP() {
  return [...ROOKIES_2026].sort((a, b) => a.adp - b.adp);
}

